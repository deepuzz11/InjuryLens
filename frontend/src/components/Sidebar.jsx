  const logout    = useStore((s) => s.logout)
  const goals     = useStore((s) => s.goals)
  const injuries  = useStore((s) => s.injuries)
  const getRiskAlerts = useStore((s) => s.getRiskAlerts)
  const alertCount = getRiskAlerts().length
  const activeInjuries = injuries.filter((i) => !i.resolved).length

